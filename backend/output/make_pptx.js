#!/usr/bin/env node
/**
 * make_pptx.js — AI Research Agent presentation generator
 * Called by pptx_exporter.py via subprocess.
 *
 * Reads:  C:/Users/bachc/AppData/Local/Temp/pptx_input.json
 *         { topic: string, slides: [{num, title, bullets[], note}] }
 * Writes: C:/Users/bachc/AppData/Local/Temp/output.pptx
 **/

"use strict";

const pptxgen = require("pptxgenjs");
const fs = require("fs");

const os = require("os");
const INPUT_PATH = require("path").join(os.tmpdir(), "pptx_input.json");
const OUTPUT_PATH = require("path").join(os.tmpdir(), "output.pptx");

// Read input
let topic, slides;
try {
  const raw = JSON.parse(fs.readFileSync(INPUT_PATH, "utf-8"));
  topic = raw.topic || "Research Report";
  slides = raw.slides || [];
} catch (e) {
  console.error("Failed to read input JSON:", e.message);
  process.exit(1);
}

if (!slides.length) {
  console.error("No slides found in input.");
  process.exit(1);
}

// Design tokens
const C = {
  dark: "0A2342",
  mid: "065A82",
  accent: "00C2CB",
  light: "F0F7FF",
  card: "FFFFFF",
  border: "D8E8F5",
  text: "1A2B3C",
  muted: "7A9BB5",
  white: "FFFFFF",
};
const FONT = "Calibri";
const mkShadow = () => ({ type: "outer", color: "000000", blur: 8, offset: 2, angle: 135, opacity: 0.12 });

function addTitleSlide(pres) {
  const slide = pres.addSlide();
  slide.background = { color: C.dark };

  slide.addShape(pres.shapes.RECTANGLE, {
    x: 0, y: 0, w: 0.18, h: 5.625,
    fill: { color: C.accent }, line: { color: C.accent },
  });

  slide.addText(topic, {
    x: 0.45, y: 1.4, w: 9.1, h: 1.7,
    fontSize: 38, bold: true, color: C.white,
    fontFace: FONT, align: "left", valign: "middle", wrap: true, margin: 0,
  });

  slide.addText("AI-Powered Research Report", {
    x: 0.45, y: 3.2, w: 7, h: 0.5,
    fontSize: 15, color: C.accent, fontFace: FONT, align: "left", margin: 0,
  });

  const today = new Date().toLocaleDateString("en-US", { year: "numeric", month: "long", day: "numeric" });
  slide.addText(today, {
    x: 0.45, y: 3.8, w: 5, h: 0.35,
    fontSize: 11, color: C.muted, fontFace: FONT, align: "left", margin: 0,
  });

  slide.addShape(pres.shapes.RECTANGLE, {
    x: 0, y: 5.3, w: 10, h: 0.325,
    fill: { color: C.mid }, line: { color: C.mid },
  });
  slide.addText("Generated by Autonomous AI Research Agent  |  Groq · Tavily · ArXiv · LangGraph", {
    x: 0.3, y: 5.3, w: 9.4, h: 0.325,
    fontSize: 9, color: C.white, fontFace: FONT,
    align: "left", valign: "middle", margin: 0,
  });
}

function addTocSlide(pres) {
  const slide = pres.addSlide();
  slide.background = { color: C.light };

  slide.addShape(pres.shapes.RECTANGLE, {
    x: 0, y: 0, w: 10, h: 1.0,
    fill: { color: C.dark }, line: { color: C.dark },
  });
  slide.addText("Table of Contents", {
    x: 0.5, y: 0, w: 9, h: 1.0,
    fontSize: 26, bold: true, color: C.white,
    fontFace: FONT, align: "left", valign: "middle", margin: 0,
  });
  slide.addShape(pres.shapes.RECTANGLE, {
    x: 0, y: 1.0, w: 10, h: 0.05,
    fill: { color: C.accent }, line: { color: C.accent },
  });

  const half = Math.ceil(slides.length / 2);
  const colW = 4.4;
  const startY = 1.25;
  const rowH = 0.48;

  slides.forEach(function (s, i) {
    const col = i < half ? 0 : 1;
    const row = i < half ? i : i - half;
    const x = col === 0 ? 0.4 : 5.2;
    const y = startY + row * rowH;
    if (y + rowH > 5.4) return;

    slide.addShape(pres.shapes.OVAL, {
      x: x, y: y + 0.05, w: 0.32, h: 0.32,
      fill: { color: C.mid }, line: { color: C.mid },
    });
    slide.addText(String(i + 1), {
      x: x, y: y + 0.05, w: 0.32, h: 0.32,
      fontSize: 9, bold: true, color: C.white,
      fontFace: FONT, align: "center", valign: "middle", margin: 0,
    });
    slide.addText(s.title, {
      x: x + 0.4, y: y, w: colW, h: 0.4,
      fontSize: 13, color: C.text,
      fontFace: FONT, align: "left", valign: "middle", margin: 0,
    });
  });
}

function addContentSlide(pres, slideData, slideNum, total) {
  const s = pres.addSlide();
  s.background = { color: C.light };

  s.addShape(pres.shapes.RECTANGLE, {
    x: 0, y: 0, w: 10, h: 0.95,
    fill: { color: C.dark }, line: { color: C.dark },
  });
  s.addShape(pres.shapes.RECTANGLE, {
    x: 0, y: 0.95, w: 10, h: 0.055,
    fill: { color: C.accent }, line: { color: C.accent },
  });
  s.addText(slideData.title, {
    x: 0.45, y: 0, w: 8.4, h: 0.95,
    fontSize: 22, bold: true, color: C.white,
    fontFace: FONT, align: "left", valign: "middle", wrap: true, margin: 0,
  });
  s.addText(slideNum + " / " + total, {
    x: 8.5, y: 0.2, w: 1.2, h: 0.5,
    fontSize: 9, color: C.muted, fontFace: FONT, align: "right", margin: 0,
  });

  const bullets = (slideData.bullets || []).filter(function (b) { return b && b.trim(); });
  if (bullets.length === 0) return;

  const useCards = bullets.length >= 4;

  if (useCards) {
    const cols = 2;
    const cardW = 4.6;
    const cardH = bullets.length <= 6 ? 1.5 : 1.15;
    const gapX = 0.2;
    const gapY = 0.16;
    const startX = 0.3;
    const startY = 1.2;

    bullets.forEach(function (b, i) {
      const col = i % cols;
      const row = Math.floor(i / cols);
      const x = startX + col * (cardW + gapX);
      const y = startY + row * (cardH + gapY);
      if (y + cardH > 5.35) return;

      s.addShape(pres.shapes.RECTANGLE, {
        x: x, y: y, w: cardW, h: cardH,
        fill: { color: C.card }, line: { color: C.border, width: 0.75 },
        shadow: mkShadow(),
      });
      s.addShape(pres.shapes.RECTANGLE, {
        x: x, y: y, w: 0.07, h: cardH,
        fill: { color: C.accent }, line: { color: C.accent },
      });
      s.addText(b, {
        x: x + 0.18, y: y + 0.1, w: cardW - 0.28, h: cardH - 0.2,
        fontSize: 12.5, color: C.text, fontFace: FONT,
        align: "left", valign: "top", wrap: true, margin: 0,
      });
    });

  } else {
    const startY = 1.22;
    const rowH = bullets.length <= 3 ? 0.95 : 0.75;

    bullets.forEach(function (b, i) {
      const y = startY + i * rowH;

      s.addShape(pres.shapes.OVAL, {
        x: 0.35, y: y + 0.08, w: 0.36, h: 0.36,
        fill: { color: C.mid }, line: { color: C.mid },
      });
      s.addText(String(i + 1), {
        x: 0.35, y: y + 0.08, w: 0.36, h: 0.36,
        fontSize: 10, bold: true, color: C.white,
        fontFace: FONT, align: "center", valign: "middle", margin: 0,
      });
      s.addText(b, {
        x: 0.88, y: y, w: 8.82, h: rowH,
        fontSize: 14, color: C.text, fontFace: FONT,
        align: "left", valign: "middle", wrap: true, margin: 0,
      });
    });
  }

  s.addShape(pres.shapes.RECTANGLE, {
    x: 0, y: 5.45, w: 10, h: 0.175,
    fill: { color: C.dark }, line: { color: C.dark },
  });
  s.addText(slideData.title, {
    x: 0.3, y: 5.45, w: 9.4, h: 0.175,
    fontSize: 7.5, color: C.muted, fontFace: FONT,
    align: "left", valign: "middle", margin: 0,
  });
}

function addClosingSlide(pres) {
  const slide = pres.addSlide();
  slide.background = { color: C.dark };

  slide.addShape(pres.shapes.RECTANGLE, {
    x: 0, y: 0, w: 0.18, h: 5.625,
    fill: { color: C.accent }, line: { color: C.accent },
  });
  slide.addText("Thank You", {
    x: 0.45, y: 1.3, w: 9.1, h: 1.1,
    fontSize: 44, bold: true, color: C.white,
    fontFace: FONT, align: "left", margin: 0,
  });
  slide.addText("Research topic: " + topic, {
    x: 0.45, y: 2.55, w: 9, h: 0.5,
    fontSize: 14, color: C.accent, fontFace: FONT, align: "left", margin: 0,
  });
  slide.addText("Generated by Autonomous AI Research Agent\nPowered by Groq  |  Tavily  |  ArXiv  |  LangGraph", {
    x: 0.45, y: 3.25, w: 9, h: 0.8,
    fontSize: 11, color: C.muted, fontFace: FONT, align: "left", margin: 0,
  });
  slide.addShape(pres.shapes.RECTANGLE, {
    x: 0, y: 5.3, w: 10, h: 0.325,
    fill: { color: C.mid }, line: { color: C.mid },
  });
}

// Main
(async function () {
  try {
    const pres = new pptxgen();
    pres.layout = "LAYOUT_16x9";
    pres.author = "AI Research Agent";
    pres.title = topic;
    pres.subject = "AI Research Report";

    addTitleSlide(pres);
    if (slides.length > 2) { addTocSlide(pres); }
    slides.forEach(function (s, i) {
      addContentSlide(pres, s, i + 1, slides.length);
    });
    addClosingSlide(pres);

    await pres.writeFile({ fileName: OUTPUT_PATH });
    console.log("OK:" + OUTPUT_PATH);

  } catch (err) {
    console.error("PPTX error:", err.message);
    process.exit(1);
  }
})();